package com.company;

public class Calculator {
    public int add(int a, int b) {
        return a + b;
    }

    public int minus(int a, int b) {
        return a - b;
    }

    public long multiply(int a, int b) {
        long c = a;

        return c * b;
    }

    public int division(int a, int b) {

        return a / b;


    }
}
