package com.company.util;

public class Checker {
    public boolean isEven(int number) {
        if (number % 2 == 0) {
            return true;
        } else

            return false;
    }

    public boolean isOdd(int number) {
        if(number % 2 != 0){
            return true;
        }else
            return false;

    }
}
